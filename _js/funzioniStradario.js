/* Queste Funzioni devono essere incluse nella pagina da dove viene richiamato lo stradario */
function OggettoStrada(
	CodiceEnte,
	CodStrada,
	Strada,
	TipoStrada,
	CodTipoStrada,
	Frazione,
	CodFrazione
){
	this.CodiceEnte = CodiceEnte;
	this.CodStrada = CodStrada;
	this.Strada = Strada;
	this.TipoStrada = TipoStrada;
	this.CodTipoStrada = CodTipoStrada;
	this.Frazione = Frazione;
	this.CodFrazione = CodFrazione;
}